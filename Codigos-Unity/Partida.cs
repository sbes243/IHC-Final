using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Partida
{    
    public float x;
    public float y;
    public float z;

    public Partida(float _x = 0, float _y = 0, float _z=0)
    {        
        this.x = _x;
        this.y = _y;
        this.z = _z;
    }
} 
